from __future__ import annotations

import json
import re
import shutil
import uuid
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Dict


def _slugify(text: str) -> str:
    normalized = re.sub(r"[^a-zA-Z0-9]+", "-", text.strip().lower()).strip("-")
    return normalized or "run"


def create_run_directory(
    *,
    experiment_root: str | Path = "artifacts/experiments",
    experiment_name: str = "multimodal-train",
) -> tuple[str, Path]:
    ts = datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")
    run_token = uuid.uuid4().hex[:8]
    run_id = f"{ts}_{_slugify(experiment_name)}_{run_token}"
    run_dir = Path(experiment_root) / run_id
    run_dir.mkdir(parents=True, exist_ok=False)
    return run_id, run_dir


def _write_json(path: Path, payload: Dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, ensure_ascii=False), encoding="utf-8")


def record_experiment_run(
    *,
    experiment_root: str | Path,
    experiment_name: str,
    summary: Dict[str, Any],
    summary_path: str | Path,
) -> Dict[str, str]:
    run_id, run_dir = create_run_directory(
        experiment_root=experiment_root,
        experiment_name=experiment_name,
    )

    summary_file = run_dir / "summary.json"
    _write_json(summary_file, summary)

    history = summary.get("fit_result", {}).get("history", {})
    val_pr_auc_hist = history.get("val_pr_auc", []) if isinstance(history, dict) else []
    val_f1_hist = history.get("val_f1", []) if isinstance(history, dict) else []
    val_recall_hist = history.get("val_recall", []) if isinstance(history, dict) else []
    final_val_pr_auc = float(val_pr_auc_hist[-1]) if val_pr_auc_hist else None
    final_val_f1 = float(val_f1_hist[-1]) if val_f1_hist else None
    final_val_recall = float(val_recall_hist[-1]) if val_recall_hist else None

    metrics = {
        "run_id": run_id,
        "experiment_name": experiment_name,
        "generated_at_utc": datetime.now(timezone.utc).isoformat(),
        "epochs_ran": summary.get("fit_result", {}).get("epochs_ran"),
        "best_val_loss": summary.get("fit_result", {}).get("best_val_loss"),
        "final_val_pr_auc": final_val_pr_auc,
        "final_val_f1": final_val_f1,
        "final_val_recall": final_val_recall,
        "test_loss": summary.get("test_loss"),
        "test_metrics": summary.get("test_metrics"),
        "checkpoint_path": summary.get("fit_result", {}).get("checkpoint_path"),
    }
    metrics_file = run_dir / "metrics.json"
    _write_json(metrics_file, metrics)

    config_payload = {
        "dataset_config": summary.get("dataset_config"),
        "training_config": summary.get("training_config"),
        "runtime_profile": summary.get("runtime_profile"),
        "hardware_profile": summary.get("hardware_profile"),
        "runtime_notes": summary.get("runtime_notes"),
    }
    config_file = run_dir / "config.json"
    _write_json(config_file, config_payload)

    # Keep a copy of the latest summary generated by the training entrypoint.
    source_summary_path = Path(summary_path)
    latest_copy_path = run_dir / "latest_summary_copy.json"
    if source_summary_path.exists():
        shutil.copy2(source_summary_path, latest_copy_path)

    return {
        "run_id": run_id,
        "run_dir": str(run_dir),
        "summary_json": str(summary_file),
        "metrics_json": str(metrics_file),
        "config_json": str(config_file),
    }
